
public class SkillPosition extends FantasyPlayer{
	
	private int scrimmageYards;
	private int touchdowns;
	private int touches;
	
	public int getScrimmageYards() {return scrimmageYards;}
	
	public int getTouchdowns() {return touchdowns;}
	
	public int touches() {return touches;}
	
	public void setScrimmageYards(int scrimmageYards) {this.scrimmageYards = scrimmageYards;}
	
	public void setTouchdowns(int touchdowns) {this.touchdowns = touchdowns;}
	
	public void setTouches(int touches) {this.touches = touches;}
}
